// Helper to get all road points for a specific traffic level
export function getRoadsByTrafficLevel(level: RoadSegment['trafficLevel']): RoadSegment[] {
  return roadNetwork.filter(road => road.trafficLevel === level);
}

// Helper to get total vehicle capacity estimate
export function estimateRoadVehicleCapacity(): number {
  return roadNetwork.reduce((total, road) => {
    const lengthKm = road.points.length * 0.5; // Rough estimate
    const vehiclesPerKm = road.lanes * 20; // ~20 vehicles per lane per km
    return total + lengthKm * vehiclesPerKm;
  }, 0);
}
